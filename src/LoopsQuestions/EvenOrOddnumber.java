package LoopsQuestions;
import java.util.*;

public class EvenOrOddnumber {
	public static void main(String[] args) {
		Scanner name=new Scanner(System.in);
	    int n=name.nextInt();
	    if(n%2==0) {
	    	System.out.println("It is a Even Number");
	    }
	    else {
	    	System.out.println("It is Odd Number");
	    }
		
	}
}
